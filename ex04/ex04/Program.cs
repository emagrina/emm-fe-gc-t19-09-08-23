namespace ConsoleAplication
{
    class Program
    {
        static void Main(string[] args)
        {

            int n = 1;

            n += 77;
            Console.WriteLine(n + " + 77 = " + n);

            n -= 3;
            Console.WriteLine(n + " - 3 = " + n);

            n *= 2;
            Console.WriteLine(n + " * 2 = " + n);

        }
    }
}

